func ResolveDomains(domains []string) ([]string, error) {
	var ips []string

	for _, domain := range domains {
		addrs, err := net.LookupHost(domain)
		if err != nil {
			return nil, fmt.Errorf("ошибка при резолве %s: %v", domain, err)
		}
		ips = append(ips, addrs...)
	}

	return ips, nil
}

